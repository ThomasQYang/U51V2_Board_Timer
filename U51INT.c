#include	"U51V2.h"

void init_INT_M_P(unsigned char I,bit M,bit P)
{
	if(I==int_I0){
		IT0=M;
		PX0=P;
		EX0=1;
	}
	else{
		IT1=M;
		PX1=P;
		EX1=1;
	}
}
